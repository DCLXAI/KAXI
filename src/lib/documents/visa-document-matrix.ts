export function matrixCompletenessSummary(seeds: VisaDocumentRequirementSeed[] = VISA_DOCUMENT_REQUIREMENT_SEEDS) {
  const uniqueCodes = new Set(seeds.map((seed) => seed.code));
  const uniqueComposite = new Set(seeds.map((seed) => [seed.visaType, seed.stayAction, seed.applicantContext, seed.documentType].join("|")));
  const validationRuleRows = seeds.filter((seed) => seed.validationRules.length > 0);
  const reviewedRows = seeds.filter((seed) => seed.reviewStatus === "APPROVED");
  const visaTypes = Array.from(new Set(seeds.map((seed) => seed.visaType))).sort();

  return {
    totalRows: seeds.length,
    uniqueCodes: uniqueCodes.size,
    uniqueCompositeRows: uniqueComposite.size,
    validationRuleRows: validationRuleRows.length,
    reviewedRows: reviewedRows.length,
    visaTypes,
  };
}
