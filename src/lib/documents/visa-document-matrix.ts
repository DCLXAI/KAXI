export type VisaDocumentReviewStatus = "PENDING" | "APPROVED" | "REJECTED";

export interface RequiredDocumentField {
  key: string;
  labelKo: string;
  labelEn: string;
  required: boolean;
}

export interface VisaDocumentRequirementSeed {
  code: string;
  visaType: string;
  stayAction: "issuance" | "extension" | "change" | "permanent_residence";
  applicantContext: string;
  documentType: string;
  labelKo: string;
  labelEn: string;
  required: boolean;
  validityDays: number | null;
  issuer: string;
  requiredFields: RequiredDocumentField[];
  validationRules: string[];
  sourceRefs: string[];
  sourceUrl: string;
  sourceType: "official_government" | "official_law" | "internal_analysis";
  lastCheckedAt: string;
  checkedBy: string;
  reviewStatus: VisaDocumentReviewStatus;
  notes?: string;
}

const CHECKED_BY = "phase0_seed_unreviewed";
const LAST_CHECKED_AT = "2026-07-03";
const LAW_ATTACHMENTS = "immigration-rule-documents-attachments";
const LAW_STATUS_TABLE = "immigration-decree-long-term-status-table";
const HIKOREA_MANUAL = "hikorea-d2-d4-d10-e7-f2-f5-requirements";
const HIKOREA_FORMS = "hikorea-forms-document-checklist";
const STUDY_DOCS = "studyinkorea-visa-documents";
const VISA_PORTAL = "visa-portal-visa-types";
const POSITIVE_NUMERIC_FIELD_KEYS = new Set(["balance", "paid_amount", "income_amount", "wage"]);
const PERCENTAGE_FIELD_KEYS = new Set(["attendance_rate"]);

function field(key: string, labelKo: string, labelEn: string, required = true): RequiredDocumentField {
  return { key, labelKo, labelEn, required };
}

function rulesFor(fields: RequiredDocumentField[], validityDays: number | null, extras: string[] = []): string[] {
  const requiredKeys = new Set(fields.filter((item) => item.required).map((item) => item.key));
  return [
    "required_document_present",
    "issuer_present",
    ...fields.filter((item) => item.required).map((item) => `field:${item.key}:present`),
    ...[...requiredKeys]
      .filter((key) => POSITIVE_NUMERIC_FIELD_KEYS.has(key))
      .map((key) => `numeric_positive:${key}`),
    ...[...requiredKeys]
      .filter((key) => PERCENTAGE_FIELD_KEYS.has(key))
      .map((key) => `numeric_range:${key}:0:100`),
    ...(validityDays ? [`issue_date_within:${validityDays}:days`] : []),
    ...extras,
  ];
}

function req(input: Omit<VisaDocumentRequirementSeed, "checkedBy" | "lastCheckedAt" | "reviewStatus" | "validationRules" | "required" | "sourceType"> & {
  required?: boolean;
  sourceType?: VisaDocumentRequirementSeed["sourceType"];
  validationRules?: string[];
}): VisaDocumentRequirementSeed {
  const required = input.required ?? true;
  return {
    ...input,
    required,
    sourceType: input.sourceType || "official_government",
    checkedBy: CHECKED_BY,
    lastCheckedAt: LAST_CHECKED_AT,
    reviewStatus: "PENDING",
    validationRules: input.validationRules || rulesFor(input.requiredFields, input.validityDays),
  };
}

